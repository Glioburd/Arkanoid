#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	char	*m;
	size_t	i;

	i = 0;
	m = (char*)str;
	while (i < n)
	{
		m[i] = (char)c;
		i++;
	}
	return (str);
}
