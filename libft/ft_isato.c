#include "libft.h"

int		ft_isato(const char *str)
{
	while (ft_isspace(*str) == 1)
		str++;
	if (*str == '-' || *str == '+')
		str++;
	return (*str >= '0' && *str <= '9') ? 1 : 0;
}
